#include <unistd.h>

void	gug(char m)
{
	write(1, &m, 1);
}

void	ft_print_comb2(void)
{
	int	a;
	int	b;

	a = 0;
	b = 0;
	while (a < 100)
	{
		b = a + 1;
		while (b < 100)
		{
			gug(a / 10 + '0');
			gug(a % 10 + '0');
			gug(' ');
			gug(b / 10 + '0');
			gug(b % 10 + '0');
			if ((a / 10 != 9) || (a % 10 != 8))
			{
				gug(',');
				gug(' ');
			}
			b++;
		}
		a++;
	}
}
