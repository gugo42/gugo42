#include <unistd.h>

int	ft_str_is_lowercase(char *str)
{
	int	g;

	g = 0;
	if (*str == '\0')
		return (1);
	while (str[g] != '\0')
	{
		if (str[g] < 'a' || str[g] > 'z')
			return (0);
		g++;
	}
	return (1);
}
