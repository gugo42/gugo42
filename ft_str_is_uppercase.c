#include <unistd.h>

int	ft_str_is_uppercase(char *str)
{
	int	k;

	k = 0;
	while (str[k])
	{
		if (!((str[k] >= 'A') && (str[k] <= 'Z')))
			return (0);
		k++;
	}
	return (1);
}
