#include <unistd.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	g;

	g = 0;
	while (s1[g] == s2[g])
	{
		if (s1[g] == '\0' && s2[g] == '\0')
			return (0);
		g++;
	}
	return (s1[g] - s2[g]);
}
