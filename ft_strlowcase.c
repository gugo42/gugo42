#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	int	u;

	u = 0;
	while (str[u])
	{
		if ((str[u] >= 'A') && (str[u] <= 'Z'))
				str[u] = str[u] + 32;
			u++;
	}
	return (str);
}
