#include <unistd.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	a;

	a = 0;
	if (n == 0)
		return (0);
	while (s1[a] == s2[a] && (a < n - 1) && s1[a])
		a++;
	return (s1[a] - s2[a]);
}
