#include <unistd.h>

char	*ft_strupcase(char *str)
{
	int	g;

	g = 0;
	while (str[g])
	{
		if (str[g] >= 'a' && str[g] <= 'z')
			str[g] = str[g] - 32;
		g++;
	}
	return ((char *)str);
}
